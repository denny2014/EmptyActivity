package com.zet.parser;

public class DETAIL {
	private String SET_YEAR;
	private String FILE_ID;
	private String ELEMENT_CODE;
	private String ELEMENT_NAME;
	private String PROC_CONTENT;
	public String getSET_YEAR() {
		return SET_YEAR;
	}
	public void setSET_YEAR(String sET_YEAR) {
		SET_YEAR = sET_YEAR;
	}
	public String getFILE_ID() {
		return FILE_ID;
	}
	public void setFILE_ID(String fILE_ID) {
		FILE_ID = fILE_ID;
	}
	public String getELEMENT_CODE() {
		return ELEMENT_CODE;
	}
	public void setELEMENT_CODE(String eLEMENT_CODE) {
		ELEMENT_CODE = eLEMENT_CODE;
	}
	public String getELEMENT_NAME() {
		return ELEMENT_NAME;
	}
	public void setELEMENT_NAME(String eLEMENT_NAME) {
		ELEMENT_NAME = eLEMENT_NAME;
	}
	public String getPROC_CONTENT() {
		return PROC_CONTENT;
	}
	public void setPROC_CONTENT(String pROC_CONTENT) {
		PROC_CONTENT = pROC_CONTENT;
	}
	@Override
	public String toString() {
		return "DETAIL [SET_YEAR=" + SET_YEAR + ", FILE_ID=" + FILE_ID
				+ ", ELEMENT_CODE=" + ELEMENT_CODE + ", ELEMENT_NAME="
				+ ELEMENT_NAME + ", PROC_CONTENT=" + PROC_CONTENT + "]";
	}
}
