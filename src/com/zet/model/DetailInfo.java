package com.zet.model;

import java.util.List;

/**
 * 详情信息
 * Created by macchen on 15/4/11.
 */
public class DetailInfo {

    /**
     * 标题
     */
    private String title;

    /**
     * 提要
     */
    private List<TiYaoInfo> tiyao;
    /**
     * 提要1
     */
    private List<TiYaoInfo> tiyao1;

    /**
     * 附件
     */
    private List<FileInfo> fujian;
    /**
     * 相关文档
     */
    private List<DocInfo> relative;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public List<TiYaoInfo> getTiyao() {
        return tiyao;
    }

    public void setTiyao(List<TiYaoInfo> tiyao) {
        this.tiyao = tiyao;
    }

    public List<TiYaoInfo> getTiyao1() {
        return tiyao1;
    }

    public void setTiyao1(List<TiYaoInfo> tiyao1) {
        this.tiyao1 = tiyao1;
    }

    public List<FileInfo> getFujian() {
        return fujian;
    }

    public void setFujian(List<FileInfo> fujian) {
        this.fujian = fujian;
    }

    public List<DocInfo> getRelative() {
        return relative;
    }

    public void setRelative(List<DocInfo> relative) {
        this.relative = relative;
    }
}
